        tabLayout = findViewById(R.id.tablayout);

        viewPagerAdapter = new ViewPagerAdapter(getSupportFragmentManager(),0);
        viewPagerAdapter.addFragment(new ModelExam_Fragment(), "All");
        viewPagerAdapter.addFragment(new ModelExam_Fragment(), "Maths");
