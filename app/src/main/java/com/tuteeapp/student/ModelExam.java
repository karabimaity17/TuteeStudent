    BottomNavigationView bottomnav;
    ImageView img_back;


    private ViewPager viewPager;
    private TabLayout tabLayout;

    private int[] tabIcons = {
            R.drawable.book,
            R.drawable.math,
            R.drawable.physics,
            R.drawable.english,
            R.drawable.chemistry};

    ViewPagerAdapter viewPagerAdapter;
